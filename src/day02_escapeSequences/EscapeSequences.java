package day02_escapeSequences;

public class EscapeSequences {


    public static void main(String[] args) {


        System.out.println("Hello Students\nToday we have  Java class"); // \n sequence adds a new line

        System.out.println("----------------------------------------------");

        System.out.println("\tJava Programming Language"); // \t gives us space when we run horizontal tab
        System.out.println("\t\t\tJava Developer Course Batch 1");

        System.out.println("-----------------------------------------");

        System.out.println("\\");

        System.out.println("-----------------------------------------");

        System.out.println("My favorite TV shows is \"The Walking Dead\"");





    }
}


/*
Common Escape Sequences:

  \n --- Newline --- Advances the cursor to the next line for subsequent printing
  \t --- Horizontal tab --- Cause the cursor to skip over to the next tab stop
  \\ ---- Backslash --- Causes a backslash to be printed
  \" --- Double quote --- Cause a double quotation mark to be printed

 */